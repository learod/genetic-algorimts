    private void dibujar() {
            
            
            java.awt.Graphics g = jPanel9.getGraphics();
            java.awt.Dimension d = jPanel9.getSize();
            apAncho = d.width;
            apAlto = d.height;
            g.setColor(java.awt.Color.WHITE);
            g.fillRect(0, 0, apAncho, apAlto);
            g.setColor(java.awt.Color.BLACK);
            g.drawRect(0, 0, apAncho-1, apAlto-1);
            xmin = -0.2;
	    xmax = this.listaValores.length;
	    ymin = -0.2;
	    ymax = 1.2;
            int apCero=(int)( (0-xmin) * (apAncho-1) / (xmax-xmin) );
            int ypCero= (int)( (0-ymin) * (apAlto-1) / (ymax-ymin) );
            ypCero = apAlto - ypCero;
            g.setColor(java.awt.Color.black);
            g.drawLine(0,ypCero,(int)apAncho,ypCero);
            g.drawLine(apCero,apAlto,apCero,0);
            int xp,yp;
            yp = (int)( (0.5-ymin) * (apAlto-1) / (ymax-ymin) );
            xp=(int)( ( 0.5 - xmin) * (apAncho-1) / (xmax-xmin) );
            g.drawLine(apCero, ypCero, xp, ypCero);
            g.drawString("0.5",(int)apCero,(int)yp);

            if (this.listaValores.length > 0){
                int j1, j2;
                j1 = ValorY1(0);
                for (int i = 0; i < this.listaValores.length - 1; i++) {
                    j2 = ValorY1(i + 1);
                    
                    int ap=(int)( (i-xmin) * (apAncho-1) / (xmax-xmin) );
                    int ap2=(int)( ((i +1)-xmin) * (apAncho-1) / (xmax-xmin) );
                    if (i%5==0){
                         g.setColor(java.awt.Color.BLUE);
                         g.drawString(""+i,(int)ap,(int)ypCero + 10);
                    }
                    g.setColor(java.awt.Color.RED);
                    g.drawLine(ap, j1, ap2, j2);
                    j1 = j2;
                }
            }
          
        }

        private int ValorY1(int valor) {
            double x, y;
            int retorno;
            x = (valor * (xmax - xmin) / (apAncho - 1)) + xmin;
            y=this.listaValores[valor];
            retorno = (int) ((y - ymin) * (apAlto - 1) / (ymax - ymin));
            retorno = apAlto - retorno;
            return (retorno);
        }



        public void cargaLista(){
            double[] aux = new double[this.listaValores.length + 1];
            for(int i=0;i<this.listaValores.length;i++){
                aux[i]=this.listaValores[i];
            }
            aux[this.listaValores.length]=promedioPoblacion();
            this.listaValores=aux;
        }


